// Factory: basic key-info object (type, virtual-key code, name, alt-code, alt-name, scan-code)
function create_pc_key_info(type, vCode, name, aCode, aName, sCode) {
  return { type, vCode, name, aCode, aName, sCode };
}
// Factory: keyboard-layout key-info — adds keyId, row, col, and rect [x,y,w,h] for visual positioning
function kbd_create_pc_key_info(client, value, keyName, altCode, altName, scanCode, keyId, row, col, rect) {
  var info = {
    type: client,
    vCode: value,
    name: keyName,
    aCode: altCode,
    aName: altName,
    sCode: scanCode,
    keyId: keyId,
    row: row,
    col: col,
    rect: rect
  };
  return info;
}
// Factory: selectable key-info used in key‑picker UIs (keyboard + mouse layouts)
function create_pc_select_key_info(keyType, value, keyName, altCode, altName, scanCode, keyId, rect) {
  var info = {
    type: keyType,
    vCode: value,
    name: keyName,
    aCode: altCode,
    aName: altName,
    sCode: scanCode,
    keyId: keyId,
    rect: rect
  };
  return info;
}
// Clone: shallow-copy a key-info object
function kbd_clone_pc_key_info(client) {
  var info = {
    type: client.type,
    vCode: client.vCode,
    name: client.name,
    aCode: client.aCode,
    aName: client.aName,
    sCode: client.sCode,
    keyId: client.keyId,
    row: client.row,
    col: client.col,
    rect: client.rect
  };
  return info;
}
// Simple string concatenation helper
function wsn(a, b) {
  return a + b;
}

